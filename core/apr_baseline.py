def extract_function_code(source_code, func_name):
    """
    Heuristic to extract a C function by name using regex and brace matching.
    Fixed to matching modern C signatures more robustly, especially `main`.
    """
    # Improved regex to capture C function definitions more flexibly. 
    # Example: int main() { or int main(int argc, char *argv[]) {
    pattern = re.compile(
        r'\b(?:int|void|char|double|float|long|unsigned|short|struct|static)?\s*\*?\s*' + 
        re.escape(func_name) + r'\s*\([^)]*\)\s*\{', 
        re.MULTILINE
    )
    match = pattern.search(source_code)
    if not match:
        # Fallback for main specifically id it lacks return type e.g. main() {
        if func_name == "main":
            pattern = re.compile(r'\bmain\s*\([^)]*\)\s*\{', re.MULTILINE)
            match = pattern.search(source_code)
            if not match:
                return None, -1, -1
        else:
            return None, -1, -1

    start_idx = match.start()
    open_braces = 0
    in_func = False
    
    for i in range(match.end() - 1, len(source_code)):
        char = source_code[i]
        if char == '{':
            open_braces += 1
            in_func = True
        elif char == '}':
            open_braces -= 1
            if in_func and open_braces == 0:
                return source_code[start_idx:i+1], start_idx, i+1
                
    return None, -1, -1

import google.generativeai as genai
from dotenv import load_dotenv

load_dotenv()
GEMINI_API_KEY = os.getenv("GEMINI_API_KEY")

def call_llm(prompt):
    """
    Call the LLM (Gemini) to generate a patch based on the prompt.
    """
    api_key = os.getenv("GEMINI_API_KEY")
    if not api_key:
        print("Warning: GEMINI_API_KEY environment variable not set. Using a dummy LLM.")
        return "/* THIS IS A DUMMY PATCH GENERATED BY THE MOCK LLM */\n// TODO: Implement actual LLM call using Google Generative AI API."
    
    try:
        genai.configure(api_key=api_key)
        # Using a model that was listed in the supported method
        model = genai.GenerativeModel('models/gemini-2.5-flash')
        response = model.generate_content(prompt)
        return response.text
    except Exception as e:
        error_msg = str(e)
        if "Quota" in error_msg or "limit" in error_msg:
            print("Warning: API quota limit reached. Please try again later.")
        else:
            print(f"Error calling LLM: {error_msg}")
        return None

# Đã chuyển validation logic vào core/sandbox_adapter.py
def validate_patch(patched_file_path, bug_id, dataset="codeflaws"):
    """
    Sử dụng Adapter tương ứng với bộ dataset để kiểm chứng bản vá trong hộp cát (Sandbox).
    """
    print(f"Validating patch for {bug_id} using {dataset} adapter...")
    try:
        adapter = get_sandbox_adapter(dataset, bug_id)
        return adapter.validate(patched_file_path)
    except Exception as e:
        print(f"    [Error] Cannot validate patch using adapter: {e}")
        return False, [], []

def run_apr_pipeline(dataset="codeflaws"):
    os.makedirs(EXPERIMENTS_DIR, exist_ok=True)
    tarantula_results_file = os.path.join(EXPERIMENTS_DIR, "tarantula_results.json")
    if not os.path.exists(tarantula_results_file):
        print(f"Error: {tarantula_results_file} not found. Run FL first.")
        return

    with open(tarantula_results_file, "r") as f:
        fl_results = json.load(f)

    apr_results = {}
    
    # Load previously saved progress if exists
    apr_results_file = os.path.join(EXPERIMENTS_DIR, "apr_results.json")
    if os.path.exists(apr_results_file):
        try:
            with open(apr_results_file, "r") as f:
                apr_results = json.load(f)
        except Exception:
            pass

    print("Đang chạy quy trình Automated Program Repair...")
    
    from configs.path import CODEFLAWS_RESULTS_DIR
    
    for bug_id, result_data in fl_results.items():
        if bug_id in apr_results and apr_results[bug_id].get("status") != "skipped":
            continue # Skip ALREADY evaluated bugs unless you delete apr_results.json
            
        if isinstance(result_data, dict) and 'scores' in result_data:
            scores = result_data['scores']
        else:
            scores = result_data # backward compatibility
            
        if not scores:
            continue
            
        # 1. Sort functions by suspiciousness (descending)
        sorted_funcs = sorted(scores.items(), key=lambda item: item[1], reverse=True)
        print(f"Processing bug {bug_id}...")
        
        # Load source code qua Adapter thay vì tự phân tích chuỗi Codeflaws tĩnh
        try:
            adapter = get_sandbox_adapter(dataset, bug_id)
            bug_source_path = adapter.get_source_path()
        except Exception as e:
            print(f"Error getting adapter for {bug_id}: {e}")
            continue
            
        if not os.path.exists(bug_source_path):
            print(f"Source file not found for {bug_id} at {bug_source_path}, skipping...")
            continue
            
        with open(bug_source_path, 'r') as f:
            source_code = f.read()

        for func_name, score in sorted_funcs:
            if score == 0.0:
                continue # Skip not suspicious functions
                
            print(f"  - Inspecting function '{func_name}' (Score: {score:.4f})")
            
            # 2. Extract function source
            func_code, start_idx, end_idx = extract_function_code(source_code, func_name)
            if not func_code:
                print(f"    WARNING: Could not extract function {func_name}")
                continue
                
            target_func = func_name
            
            # Enhanced Context Collection for Prompt
            failed_tests_context = ""
            json_path = os.path.join(CODEFLAWS_RESULTS_DIR, f"{bug_id}.json")
            if os.path.exists(json_path):
                try:
                    with open(json_path, 'r') as jf:
                        bug_intel = json.load(jf)
                    
                    failed_tests = [t for t in bug_intel.get("tests", []) if t.get("outcome") == "FAIL"]
                    if failed_tests:
                        # Select the first failing test to keep context window manageable
                        tc = failed_tests[0]
                        tc_name = tc.get("test_id", "Unknown")
                        tc_reason = tc.get("fail_reason", "Unknown")
                        tc_expected = tc.get("expected_output", "N/A")
                        tc_actual = tc.get("actual_output", "N/A")
                        
                        # Note: we truncate outputs if they are too long to save tokens
                        if len(tc_expected) > 500: tc_expected = tc_expected[:500] + "\n...[truncated]"
                        if len(tc_actual) > 500: tc_actual = tc_actual[:500] + "\n...[truncated]"
                        
                        failed_tests_context = f"""
### Thông tin kiểm thử thất bại (Test Case: {tc_name})
- **Lý do lỗi:** {tc_reason}
- **Kết quả mong đợi (Expected Output):**
```
{tc_expected.strip()}
```
- **Kết quả thực tế (Actual Output):**
```
{tc_actual.strip()}
```
"""
                except Exception as e:
                    print(f"    WARNING: Could not load test context for {bug_id}: {e}")

            # Combine full file context and the targeted function
            prompt = f"""Bạn là một chuyên gia sửa lỗi chương trình C/C++.
Nhiệm vụ của bạn là sửa một lỗi thuật toán hoặc biên dịch trong hàm `{func_name}` của đoạn mã dưới đây (Bug ID: {bug_id}).

{failed_tests_context}

### Toàn bộ file mã nguồn hiện tại (để hiểu scope, thư viện, và struct):
```c
{source_code}
```

### Yêu cầu:
1. Hãy tìm và sửa lỗi bên trong hàm `{func_name}`.
2. CHỈ TRẢ VỀ mã nguồn C của TỪNG HÀM `{func_name}` đã được sửa (để tôi có thể parse trực tiếp thay thế bằng Regex, bao gồm định dạng trả về, tên hàm, và ngoặc nhọn).
3. Tuyệt đối KHÔNG kèm theo lời giải thích mào đầu, KHÔNG viết lại các thư viện `#include` nằm bên ngoài hàm, và KHÔNG đưa thêm main() nếu đang sửa hàm cục bộ khác.

```c
// Bắt đầu viết lại hàm {func_name} tại đây:
"""
            
            # 3. Call LLM to get patch
            patched_func = call_llm(prompt)
            
            if not patched_func:
                print(f"    [ERROR] LLM returned Null (quá giới hạn API hoặc lỗi mạng). Bỏ qua việc sửa hàm này.")
                continue
                
            # Remove Markdown formatting specifically if present
            if patched_func:
                if patched_func.startswith("```c"):
                    patched_func = patched_func[4:]
                elif patched_func.startswith("```cpp"):
                    patched_func = patched_func[6:]
                elif patched_func.startswith("```"):
                    patched_func = patched_func[3:]
                    
                if patched_func.endswith("```"):
                    patched_func = patched_func[:-3]
                
                if patched_func.startswith("// Bắt đầu viết lại hàm"):
                    # Remove the prompt prefix if it repeated it
                    lines = patched_func.split("\n")
                    if len(lines) > 0 and lines[0].startswith("// Bắt đầu"):
                        patched_func = "\n".join(lines[1:])
                
                patched_func = patched_func.strip()
            
            # 4. Integrate patch into temporary source code
            patched_source = source_code[:start_idx] + patched_func + source_code[end_idx:]
            
            tmp_source_path = os.path.join(EXPERIMENTS_DIR, f"tmp_{bug_id}.c")
            with open(tmp_source_path, 'w') as f:
                f.write(patched_source)
                
            # Copy real accepted code for evaluation baseline!
            import shutil
            # Đoạn này sau này cần đưa vào Adapter nếu dataset không theo đuôi phân cách Codeflaws
            bug_dir = os.path.dirname(bug_source_path)
            bug_file_prefix = "-".join(bug_id.split("-bug-")[0].split("-"))
            bug_file_suffix_accepted = bug_id.split("-bug-")[1].split("-")[1]
            accepted_source = os.path.join(bug_dir, f"{bug_file_prefix}-{bug_file_suffix_accepted}.c")
            os.makedirs(os.path.join(EXPERIMENTS_DIR, "correct_patches"), exist_ok=True)
            if os.path.exists(accepted_source):
                shutil.copy2(accepted_source, os.path.join(EXPERIMENTS_DIR, "correct_patches", f"{bug_id}_accepted.c"))
                
            # 5. Validation
            is_valid, post_passed_tests, post_failed_tests = validate_patch(tmp_source_path, bug_id)
            
            status = "failed"
            if is_valid:
                print(f"    [SUCCESS] Found valid patch for {bug_id} in function {func_name}!")
                patch_save_path = os.path.join(PATCHES_DIR, f"{bug_id}_patch.c")
                os.rename(tmp_source_path, patch_save_path)
                status = "success"
                break # Move to next bug once fixed
            else:
                print(f"    [FAIL] Patch failed validation.")
                if os.path.exists(tmp_source_path):
                    os.remove(tmp_source_path)
                    
        # Extract init testing metrics properly
        init_passed = []
        init_failed = []
        json_path = os.path.join(CODEFLAWS_RESULTS_DIR, f"{bug_id}.json")
        if os.path.exists(json_path):
            try:
                with open(json_path, 'r') as f:
                    data = json.load(f)
                tests = data.get("tests", [])
                init_passed = [t.get("test_id") for t in tests if t.get("outcome") == "PASS"]
                init_failed = [t.get("test_id") for t in tests if t.get("outcome") == "FAIL"]
            except Exception:
                pass

        apr_results[bug_id] = {
            "status": status if 'status' in locals() else "skipped",
            "patched_function": patched_func if 'patched_func' in locals() else None,
            "selected_function": target_func if 'target_func' in locals() else None,
            "init_passed_tests": init_passed,
            "init_failed_tests": init_failed,
            "post_passed_tests": post_passed_tests if 'post_passed_tests' in locals() else [],
            "post_failed_tests": post_failed_tests if 'post_failed_tests' in locals() else []
        }

        # Lưu JSON liên tục sau mỗi bug (incremental save) thay vì đợi cạn kiệt list
        with open(os.path.join(EXPERIMENTS_DIR, "apr_results.json"), "w") as f:
            json.dump(apr_results, f, indent=4)

if __name__ == "__main__":
    run_apr_pipeline()
